#include "VgaIo.h"

/******************************************************************************
 ******************************************************************************/
VgaCharacter::VgaCharacter(char c, VgaColor fg, VgaColor bg)
{
    value = static_cast<uint16_t>(c) & 0x00FF;
    value |= static_cast<uint16_t>(fg) << 8 & 0x0F00;
    value |= static_cast<uint16_t>(bg) << 12 & 0xF000;
}

